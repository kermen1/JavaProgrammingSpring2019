package Day20;

public class ForLoop1 {

	public static void main(String[] args) {
		// Using for loop print "Love Java"
		
		
		for(int i = 1; i<=10; i++) {
			System.out.println("Love Java "+i);
		}
		
		for(int s = 5; s<=15; s++) {
			System.out.println("Number " +s);
		}
		for(int s = 20; s>=10; s--) {
			System.out.println(s);
		}
		

	}

}
