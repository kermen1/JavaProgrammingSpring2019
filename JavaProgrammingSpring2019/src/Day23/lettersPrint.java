package Day23;

public class lettersPrint {

	public static void main(String[] args) {
		
		for (int i = 1; i <= 5; i++) {
			for (char letter = 'a'; letter <= 'z'; letter++) {
				System.out.print(letter +" ");
			}
			System.out.println();
		System.out.println("****************************************************");
		}
	}

}
